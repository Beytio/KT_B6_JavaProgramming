package day16_Branching_Statements;

public class C11_DebugPractice {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println(i);
            for (int j = 0; j < 5; j++) {
                System.out.println(j);
            }
        }
    }
}
